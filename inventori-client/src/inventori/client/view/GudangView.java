    public void loadAwal(){
        try {
            List<Gudang> gudang = gudangDao.getGudang();
            if(gudang!=null){
                tableModel=new DynamicTableModel<>(gudang,Gudang.class);
                tableGudang=new JDynamicTable(tableModel);
                ScrolTabel.setViewportView(tableGudang);
                sorter=new TableRowSorter<>(tableModel);
                tableGudang.setRowSorter(sorter);
                txtCariGudang.getDocument().addDocumentListener(this);
                tableGudang.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

                    @Override
                    public void valueChanged(ListSelectionEvent e) {
                        int index=tableGudang.getSelectedRow();
                        if(index!=-1){
                            Gudang get = (Gudang) tableModel.get(tableGudang.convertRowIndexToModel(index));
                            txtKd_gd.setText(get.getKd_gd());
                            txtNm_gd.setText(get.getNm_gd());
                            cboGudang.setSelectedIndex(Integer.valueOf(get.getJenis()));
                            txtNm_gd.setEnabled(true);
                            cboGudang.setEnabled(true);
                            bUpdate.setEnabled(true);
                            bDelete.setEnabled(true);
                            bNew.setEnabled(false);
                        }
                    }
                });
            }
        } catch (RemoteException ex) {
            Logger.getLogger(GudangView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
    public boolean validInput(){
        boolean valid=false;
        if(txtKd_gd.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Kode Masih Kosong");
        }else if(txtNm_gd.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Nama Masih Kosong");
        }else if(cboGudang.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(rootPane, "Bagian Belum dipilih");
        }else{
            valid=true;
        }
        return valid;
    }
    
    public Gudang getGudang(){
        Gudang gudang=new Gudang();
        gudang.setKd_gd(txtKd_gd.getText());
        gudang.setNm_gd(txtNm_gd.getText());
        gudang.setJenis(String.valueOf(cboGudang.getSelectedIndex()));
        return gudang;
    }
    
    public void reset(){
        txtKd_gd.setText("");
        txtNm_gd.setText("");
        cboGudang.setSelectedIndex(0);
        txtNm_gd.setEnabled(false);
        cboGudang.setEnabled(false);
        bInsert.setEnabled(false);
        bNew.setEnabled(true);
        bUpdate.setEnabled(false);
        bDelete.setEnabled(false);
    }

